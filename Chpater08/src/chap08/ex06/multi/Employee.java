package chap08.ex06.multi;

public class Employee<A,B,C,D,E,F,G,H> {

	private A emp_no;
	private B name;
	private C age;
	private D salary;
	private E asset;
	private F commision;
	private G married;
	private H hobby;
	
	
	public A getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(A emp_no) {
		this.emp_no = emp_no;
	}
	public B getName() {
		return name;
	}
	public void setName(B name) {
		this.name = name;
	}
	public C getAge() {
		return age;
	}
	public void setAge(C age) {
		this.age = age;
	}
	public D getSalary() {
		return salary;
	}
	public void setSalary(D salary) {
		this.salary = salary;
	}
	public E getAsset() {
		return asset;
	}
	public void setAsset(E asset) {
		this.asset = asset;
	}
	public F getCommision() {
		return commision;
	}
	public void setCommision(F commision) {
		this.commision = commision;
	}
	public G getMarried() {
		return married;
	}
	public void setMarried(G married) {
		this.married = married;
	}
	public H getHobby() {
		return hobby;
	}
	public void setHobby(H hobby) {
		this.hobby = hobby;
	}
	
	
	
}
